package DiwaliRevisionBasics;

public class Question8 {
//Increment & Decrement
	public static void main(String[] args) {
		int a=15;
		System.out.println("Before any operation ");
		System.out.println("Number :"+a);
		++a;//a=16
		System.out.println("After ++i operation ");
		System.out.println("Number :"+a);
		a++;//after this operation a=17
		System.out.println("After i++ operation ");
		System.out.println("Number :"+a);
	}
}
